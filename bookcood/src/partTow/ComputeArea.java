package partTow;

public class ComputeArea {
	public static void main(String[] args){
		double radius;//Declare radius
		double area;//Declare radius 
		
		//Assign a radius
		radius = 20; //radius is now 20
		
		//Compute the area 
		area = radius * radius * 3.14159;
		
		//Display the results 
		System.out.print("The area for the radius " + radius + " is " + area);
	}

}
